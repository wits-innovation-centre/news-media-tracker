// Install script for the packaged local-server runtime.
// Sets up the local data directory and creates a .env.local-server configuration file.
const fs = require('fs');
const os = require('os');
const path = require('path');

const DEFAULT_PORT = 3000;
const ENV_FILE = '.env.local-server';
const APP_NAME = 'HomicideMediaTracker';

function getDefaultDataPath() {
  const platform = process.platform;
  if (platform === 'win32') {
    const appData =
      process.env.APPDATA || path.join(os.homedir(), 'AppData', 'Roaming');
    return path.join(appData, APP_NAME, 'data');
  }
  if (platform === 'darwin') {
    return path.join(
      os.homedir(),
      'Library',
      'Application Support',
      APP_NAME,
      'data',
    );
  }
  // Linux / other POSIX
  const xdgData =
    process.env.XDG_DATA_HOME || path.join(os.homedir(), '.local', 'share');
  return path.join(xdgData, 'homicide-media-tracker', 'data');
}

const dataPath = process.env.LOCAL_DATA_PATH || getDefaultDataPath();
const port = process.env.LOCAL_SERVER_PORT || DEFAULT_PORT;

// Ensure the data directory exists
if (!fs.existsSync(dataPath)) {
  fs.mkdirSync(dataPath, { recursive: true });
  console.log(`Created local data directory: ${dataPath}`);
} else {
  console.log(`Local data directory already exists: ${dataPath}`);
}

// Write (or overwrite) the configuration file
const envContent =
  [
    '# Local server configuration – generated by local-server-install',
    `LOCAL_SERVER_PORT=${port}`,
    `LOCAL_DATA_PATH=${dataPath}`,
  ].join('\n') + '\n';

fs.writeFileSync(ENV_FILE, envContent);
console.log(`Configuration written to ${ENV_FILE}`);
console.log('');
console.log('Local server installation complete.');
console.log(`  Data path : ${dataPath}`);
console.log(`  Port      : ${port}`);
console.log('');
console.log('Next steps:');
console.log('  1. Build the app if not already built: npm run build');
console.log('  2. Start the local server:              npm run local-server.start');
console.log('  3. Check server health:                 npm run local-server.health');
